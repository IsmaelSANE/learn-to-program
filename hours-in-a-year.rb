days_in_a_year = 365
days_in_leap_year = 366
hours_in_a_day = 24

puts "There are #{days_in_a_year} days in a year and #{hours_in_a_day} hours in a day"

hours_in_a_year = days_in_a_year * hours_in_a_day
hours_in_leap_year = days_in_leap_year * hours_in_a_day

puts "So there is #{hours_in_a_year} hours in a year and #{hours_in_leap_year}
hours in a leap year"
